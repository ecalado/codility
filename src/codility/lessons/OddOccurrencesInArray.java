	public int solution(int[] A) {

		Arrays.parallelSort(A);
		int count = 1;
		for (int j = 1; j < A.length; j++) {
			if (A[j] == A[j - 1]) {
				count++;
			} else {
				if (Math.floorMod(count, 2) != 0) {
					return A[j - 1];
				}

				count = 1;
			}
		}

		if (Math.floorMod(count, 2) != 0) {
			return A[A.length - 1];
		}

		return 0;
	}

